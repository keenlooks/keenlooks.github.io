/* ==========================================================================
   Tiny Transformer — a char-level GPT writing text live in the browser.
   --------------------------------------------------------------------------
   A 2-block pre-LN transformer (context 96, width 64, 4 heads, ~115k params,
   trained offline on Tiny Shakespeare by make_transformer_model.py and shipped
   int8-quantized in transformer-model.js) generates text one character at a
   time. This file implements the whole forward pass in plain JS
   (Float32Array), no ML library, with a per-position KV cache so each new
   character costs only the new position's matmuls. It also renders:
     - next-character probability bars (click one to force that character),
     - a 2x4 grid of attention heatmaps for the character being written,
     - per-character attention snapshots (click any written character).

   PARITY DISCIPLINE (same rule as the lander physics sync): at load we re-run
   the model on the file's own testVector and compare all 65 final-position
   logits to the values the trainer computed with the same dequantized weights.
   Deviation > 1e-3 shows a visible "model/page mismatch" line; otherwise a
   quiet verified tick. The model file is regenerated by training runs (same
   schema, new weights + new testVector), so nothing here hardcodes values.

   The pure math core is exported when require()d by the node test harness;
   in the browser the IIFE reads window.TRANSFORMER_MODEL and the DOM only.
   ========================================================================== */
(function () {
  'use strict';

  /* ======================= pure math core ================================ */

  function b64bytes(s) {
    if (typeof atob === 'function') {
      var bin = atob(s), a = new Uint8Array(bin.length);
      for (var i = 0; i < bin.length; i++) a[i] = bin.charCodeAt(i);
      return a;
    }
    var buf = Buffer.from(s, 'base64'), b = new Uint8Array(buf.length);
    for (var j = 0; j < buf.length; j++) b[j] = buf[j];
    return b;
  }

  function dequant(spec) {
    var q = new Int8Array(b64bytes(spec.b64).buffer);
    var f = new Float32Array(q.length);
    for (var i = 0; i < q.length; i++) f[i] = q[i] * spec.scale;
    return f;
  }

  /* Dequantize every tensor once; weight matrices are [in, out] row-major,
     applied as y = x . W + b. */
  function decodeModel(M) {
    var w = {}, k;
    for (k in M.weights) if (Object.prototype.hasOwnProperty.call(M.weights, k)) w[k] = dequant(M.weights[k]);
    var c = M.config;
    return {
      cfg: c, vocab: M.vocab, w: w,
      V: c.vocabSize, T: c.T, d: c.d, nH: c.nHeads, nL: c.nLayers,
      dff: c.dff, hd: c.d / c.nHeads
    };
  }

  function layerNorm(x, g, b, out, d) {
    var mu = 0, va = 0, i, t;
    for (i = 0; i < d; i++) mu += x[i];
    mu /= d;
    for (i = 0; i < d; i++) { t = x[i] - mu; va += t * t; }
    var inv = 1 / Math.sqrt(va / d + 1e-5);
    for (i = 0; i < d; i++) out[i] = (x[i] - mu) * inv * g[i] + b[i];
  }

  /* y = x . W + b, W is [din, dout] row-major (contiguous inner loop). */
  function matvec(x, Wm, b, out, din, dout) {
    var i, j, xi, base;
    for (j = 0; j < dout; j++) out[j] = b[j];
    for (i = 0; i < din; i++) {
      xi = x[i];
      if (xi === 0) continue;
      base = i * dout;
      for (j = 0; j < dout; j++) out[j] += xi * Wm[base + j];
    }
  }

  /* GELU, tanh approximation (must match the trainer). */
  function gelu(v) {
    return 0.5 * v * (1 + Math.tanh(0.7978845608028654 * (v + 0.044715 * v * v * v)));
  }

  /* All per-step scratch + the KV caches live in one reusable state object,
     so generation allocates nothing per character (attention snapshot rows
     excepted, and only when asked for). */
  function makeState(net) {
    var st = {
      n: 0,                                   /* tokens currently in the cache */
      K: [], Vc: [],
      x: new Float32Array(net.d), xn: new Float32Array(net.d),
      qkv: new Float32Array(3 * net.d), ao: new Float32Array(net.d),
      proj: new Float32Array(net.d),
      h1: new Float32Array(net.dff), h2: new Float32Array(net.d),
      scores: new Float32Array(net.T),
      logits: new Float32Array(net.V)
    };
    for (var L = 0; L < net.nL; L++) {
      st.K.push(new Float32Array(net.T * net.d));
      st.Vc.push(new Float32Array(net.T * net.d));
    }
    return st;
  }

  /* Feed ONE token through the model at position st.n using the KV cache.
     Leaves next-char logits in st.logits. When wantAttn, returns an array of
     nL*nH Float32Array rows (attention over positions 0..st.n inclusive),
     ordered layer 0 head 0..3, then layer 1 head 0..3. */
  function stepToken(net, st, tokId, wantAttn) {
    var d = net.d, nH = net.nH, hd = net.hd, w = net.w, pos = st.n;
    if (pos >= net.T) throw new Error('KV cache full (T=' + net.T + ')');
    var x = st.x, xn = st.xn, qkv = st.qkv, ao = st.ao, sc = st.scores;
    var i, j, t, h, L, s;
    var te = w.tok_emb, pe = w.pos_emb;
    for (i = 0; i < d; i++) x[i] = te[tokId * d + i] + pe[pos * d + i];
    var attn = wantAttn ? [] : null;
    var scale = 1 / Math.sqrt(hd);

    for (L = 0; L < net.nL; L++) {
      var p = 'blocks.' + L + '.';
      layerNorm(x, w[p + 'ln1.g'], w[p + 'ln1.b'], xn, d);
      matvec(xn, w[p + 'attn.wqkv'], w[p + 'attn.bqkv'], qkv, d, 3 * d);
      var K = st.K[L], Vc = st.Vc[L];
      for (i = 0; i < d; i++) { K[pos * d + i] = qkv[d + i]; Vc[pos * d + i] = qkv[2 * d + i]; }
      for (h = 0; h < nH; h++) {
        var b0 = h * hd, mx = -1e30;
        for (t = 0; t <= pos; t++) {
          s = 0;
          for (j = 0; j < hd; j++) s += qkv[b0 + j] * K[t * d + b0 + j];
          s *= scale;
          sc[t] = s;
          if (s > mx) mx = s;
        }
        var tot = 0;
        for (t = 0; t <= pos; t++) { s = Math.exp(sc[t] - mx); sc[t] = s; tot += s; }
        for (t = 0; t <= pos; t++) sc[t] /= tot;
        if (attn) attn.push(sc.slice(0, pos + 1));
        for (j = 0; j < hd; j++) {
          s = 0;
          for (t = 0; t <= pos; t++) s += sc[t] * Vc[t * d + b0 + j];
          ao[b0 + j] = s;
        }
      }
      matvec(ao, w[p + 'attn.wo'], w[p + 'attn.bo'], st.proj, d, d);
      for (i = 0; i < d; i++) x[i] += st.proj[i];
      layerNorm(x, w[p + 'ln2.g'], w[p + 'ln2.b'], xn, d);
      matvec(xn, w[p + 'mlp.w1'], w[p + 'mlp.b1'], st.h1, d, net.dff);
      for (i = 0; i < net.dff; i++) st.h1[i] = gelu(st.h1[i]);
      matvec(st.h1, w[p + 'mlp.w2'], w[p + 'mlp.b2'], st.h2, net.dff, d);
      for (i = 0; i < d; i++) x[i] += st.h2[i];
    }
    layerNorm(x, w['lnf.g'], w['lnf.b'], xn, d);
    matvec(xn, w['head.w'], w['head.b'], st.logits, d, net.V);
    st.n = pos + 1;
    return attn;
  }

  /* Independent full-recompute path (batch over all positions, no cache).
     Used by the test harness to cross-check the KV-cache path; the page
     itself only needs stepToken. Returns final-position logits. */
  function forwardFull(net, ids) {
    var n = ids.length, d = net.d, nH = net.nH, hd = net.hd, w = net.w;
    var X = new Float32Array(n * d);
    var i, j, t, u, h, L, s;
    for (t = 0; t < n; t++) {
      for (i = 0; i < d; i++) X[t * d + i] = w.tok_emb[ids[t] * d + i] + w.pos_emb[t * d + i];
    }
    var QKV = new Float32Array(n * 3 * d), AO = new Float32Array(n * d);
    var row = new Float32Array(d), outd = new Float32Array(d);
    var out3 = new Float32Array(3 * d), h1 = new Float32Array(net.dff);
    var scores = new Float32Array(n), scale = 1 / Math.sqrt(hd);

    for (L = 0; L < net.nL; L++) {
      var p = 'blocks.' + L + '.';
      for (t = 0; t < n; t++) {
        for (i = 0; i < d; i++) row[i] = X[t * d + i];
        layerNorm(row, w[p + 'ln1.g'], w[p + 'ln1.b'], outd, d);
        matvec(outd, w[p + 'attn.wqkv'], w[p + 'attn.bqkv'], out3, d, 3 * d);
        for (i = 0; i < 3 * d; i++) QKV[t * 3 * d + i] = out3[i];
      }
      for (t = 0; t < n; t++) {
        for (h = 0; h < nH; h++) {
          var b0 = h * hd, mx = -1e30;
          for (u = 0; u <= t; u++) {
            s = 0;
            for (j = 0; j < hd; j++) s += QKV[t * 3 * d + b0 + j] * QKV[u * 3 * d + d + b0 + j];
            s *= scale;
            scores[u] = s;
            if (s > mx) mx = s;
          }
          var tot = 0;
          for (u = 0; u <= t; u++) { s = Math.exp(scores[u] - mx); scores[u] = s; tot += s; }
          for (j = 0; j < hd; j++) {
            s = 0;
            for (u = 0; u <= t; u++) s += (scores[u] / tot) * QKV[u * 3 * d + 2 * d + b0 + j];
            AO[t * d + b0 + j] = s;
          }
        }
      }
      for (t = 0; t < n; t++) {
        for (i = 0; i < d; i++) row[i] = AO[t * d + i];
        matvec(row, w[p + 'attn.wo'], w[p + 'attn.bo'], outd, d, d);
        for (i = 0; i < d; i++) X[t * d + i] += outd[i];
        for (i = 0; i < d; i++) row[i] = X[t * d + i];
        layerNorm(row, w[p + 'ln2.g'], w[p + 'ln2.b'], outd, d);
        matvec(outd, w[p + 'mlp.w1'], w[p + 'mlp.b1'], h1, d, net.dff);
        for (i = 0; i < net.dff; i++) h1[i] = gelu(h1[i]);
        matvec(h1, w[p + 'mlp.w2'], w[p + 'mlp.b2'], outd, net.dff, d);
        for (i = 0; i < d; i++) X[t * d + i] += outd[i];
      }
    }
    for (i = 0; i < d; i++) row[i] = X[(n - 1) * d + i];
    layerNorm(row, w['lnf.g'], w['lnf.b'], outd, d);
    var logits = new Float32Array(net.V);
    matvec(outd, w['head.w'], w['head.b'], logits, d, net.V);
    return logits;
  }

  /* Softmax with temperature into a caller-owned buffer. */
  function softmax(logits, temp, out) {
    var n = logits.length, i, mx = -1e30, sum = 0, tp = temp || 1;
    for (i = 0; i < n; i++) if (logits[i] > mx) mx = logits[i];
    for (i = 0; i < n; i++) { out[i] = Math.exp((logits[i] - mx) / tp); sum += out[i]; }
    for (i = 0; i < n; i++) out[i] /= sum;
    return out;
  }

  var core = {
    decodeModel: decodeModel,
    makeState: makeState,
    stepToken: stepToken,
    forwardFull: forwardFull,
    softmax: softmax
  };

  /* Node test harness: export the math and stop (no DOM). */
  if (typeof module !== 'undefined' && module.exports) { module.exports = core; return; }
  if (typeof document === 'undefined' || typeof window === 'undefined') return;

  /* ========================== browser UI ================================= */

  var M = window.TRANSFORMER_MODEL;
  if (!M || !M.weights || !M.config) return;
  function byId(x) { return document.getElementById(x); }

  var elText = byId('tt-text'), elBars = byId('tt-bars'), attnCanvas = byId('tt-attn');
  if (!elText || !elBars || !attnCanvas) return;
  var attnCtx = attnCanvas.getContext('2d');
  if (!attnCtx) return;

  var elPrompt = byId('tt-prompt'), btnGo = byId('tt-go');
  var elTemp = byId('tt-temp'), elTempV = byId('tt-temp-val');
  var elSpeed = byId('tt-speed'), elSpeedV = byId('tt-speed-val');
  var elLen = byId('tt-len'), elLenV = byId('tt-len-val');
  var elPanel = byId('tt-panel'), elCollapse = byId('tt-collapse');
  var elVerify = byId('tt-verify'), elCaption = byId('tt-attn-caption');
  var elInfo = byId('tt-modelinfo');

  var net = decodeModel(M);
  var V = net.V, T = net.T, vocab = net.vocab;
  var SLIDE_KEEP = T - 32;                 /* when the context fills, re-prime with this many */
  var SNAP_MAX = 300;                      /* attention snapshots kept (Float32 rows, tiny) */

  var charToId = {};
  for (var ci = 0; ci < vocab.length; ci++) charToId[vocab.charAt(ci)] = ci;

  /* ---- model info + parity check (never hardcode: read the live file) ---- */
  if (elInfo) {
    var pk = M.config.paramCount ? Math.round(M.config.paramCount / 1000) + 'k parameters' : 'a small transformer';
    elInfo.textContent = pk + ', ' + net.nL + ' layers, ' + net.nH + ' attention heads, ' +
      T + ' characters of context';
  }
  (function parityCheck() {
    if (!elVerify) return;
    var tv = M.testVector;
    if (!tv || !tv.promptIds || !tv.logits || tv.logits.length !== V) {
      elVerify.textContent = 'model/page mismatch (no test vector in the model file)';
      elVerify.className = 'tt-verify tt-verify--bad';
      return;
    }
    var st = makeState(net), i;
    for (i = 0; i < tv.promptIds.length; i++) stepToken(net, st, tv.promptIds[i], false);
    var mx = 0;
    for (i = 0; i < V; i++) {
      var dv = Math.abs(st.logits[i] - tv.logits[i]);
      if (dv > mx) mx = dv;
    }
    if (mx > 1e-3) {
      elVerify.textContent = 'model/page mismatch (max logit deviation ' + mx.toExponential(2) + ')';
      elVerify.className = 'tt-verify tt-verify--bad';
      console.error('[transformer] parity check FAILED: max |Δlogit| = ' + mx +
        ' (trainSteps=' + M.config.trainSteps + '). Page logits:', Array.prototype.slice.call(st.logits),
        'expected:', tv.logits);
    } else {
      elVerify.textContent = '✓ model verified';
      elVerify.className = 'tt-verify';
      console.log('[transformer] model verified: max |Δlogit| = ' + mx.toExponential(2) +
        ' over ' + V + ' logits (trainSteps=' + M.config.trainSteps + ', valLoss=' + M.config.valLoss + ')');
    }
  })();

  /* ---- theme helpers ---- */
  function effectiveTheme() {
    var f = document.documentElement.getAttribute('data-theme');
    if (f === 'light' || f === 'dark') return f;
    return (window.matchMedia && window.matchMedia('(prefers-color-scheme: light)').matches) ? 'light' : 'dark';
  }
  function isLight() { return effectiveTheme() === 'light'; }

  /* ---- generation state ---- */
  var temperature = 0.8, speed = 20, lengthTarget = 200;
  var gen = {
    st: makeState(net),
    ctxIds: [],            /* token ids currently in the KV cache */
    chars: [],             /* { ch, span, isPrompt } for every displayed char */
    snaps: {},             /* charIndex -> { ctx: string, rows: [nL*nH Float32Array] } */
    snapOrder: [],
    probs: new Float32Array(V),
    running: false,
    finished: false,
    timer: null,
    genCount: 0,           /* generated (non-prompt) chars this run */
    promptUsed: null,
    selected: -1           /* selected char index for the attention view; -1 = follow newest */
  };
  var sortBuf = [];
  for (var si = 0; si < V; si++) sortBuf.push(si);

  function dispChar(ch) {
    if (ch === '\n') return '\\n';
    if (ch === ' ') return '␣';       /* open box */
    return ch;
  }

  /* Feed one token; when the context window is full, slide it by re-priming
     the cache with the most recent SLIDE_KEEP-1 tokens first. */
  function feedToken(tokId, wantAttn) {
    if (gen.st.n >= T) {
      var keep = gen.ctxIds.slice(gen.ctxIds.length - (SLIDE_KEEP - 1));
      gen.st.n = 0;
      gen.ctxIds = [];
      for (var i = 0; i < keep.length; i++) {
        stepToken(net, gen.st, keep[i], false);
        gen.ctxIds.push(keep[i]);
      }
    }
    var attn = stepToken(net, gen.st, tokId, wantAttn);
    gen.ctxIds.push(tokId);
    return attn;
  }

  function emitChar(ch, isPrompt) {
    var tokId = charToId[ch];
    if (tokId == null) return false;
    var idx = gen.chars.length;
    var nearBottom = elText.scrollHeight - elText.scrollTop - elText.clientHeight < 60;

    var span = document.createElement('span');
    span.className = 'tt-ch' + (isPrompt ? ' tt-ch--prompt' : ' tt-ch--new');
    span.textContent = ch;
    span.setAttribute('data-i', idx);
    elText.appendChild(span);

    var attn = feedToken(tokId, true);
    var ctxStr = '';
    for (var i = 0; i < gen.ctxIds.length; i++) ctxStr += vocab.charAt(gen.ctxIds[i]);
    gen.snaps[idx] = { ctx: ctxStr, rows: attn };
    gen.snapOrder.push(idx);
    if (gen.snapOrder.length > SNAP_MAX) delete gen.snaps[gen.snapOrder.shift()];
    gen.chars.push({ ch: ch, span: span, isPrompt: isPrompt });

    if (nearBottom) elText.scrollTop = elText.scrollHeight;
    updateBars();
    if (gen.selected < 0) renderAttn(idx);
    return true;
  }

  function sampleNext() {
    softmax(gen.st.logits, temperature, gen.probs);
    var r = Math.random(), acc = 0;
    for (var i = 0; i < V; i++) { acc += gen.probs[i]; if (r < acc) return i; }
    return V - 1;
  }

  function armTimer() {
    if (gen.timer) clearTimeout(gen.timer);
    gen.timer = setTimeout(tick, 1000 / speed);
  }
  function tick() {
    gen.timer = null;
    if (!gen.running) return;
    emitChar(vocab.charAt(sampleNext()), false);
    gen.genCount++;
    if (gen.genCount >= lengthTarget) { stopGen(true); return; }
    armTimer();
  }
  function stopGen(finished) {
    gen.running = false;
    if (gen.timer) { clearTimeout(gen.timer); gen.timer = null; }
    if (finished) gen.finished = true;
    updateGoBtn();
  }
  function updateGoBtn() {
    if (!btnGo) return;
    btnGo.textContent = gen.running ? 'Stop' : 'Generate';
    btnGo.title = gen.running ? 'Pause generation'
      : (gen.finished || gen.promptUsed !== (elPrompt ? elPrompt.value : gen.promptUsed)
        ? 'Start from the prompt' : (gen.genCount > 0 ? 'Continue generating' : 'Start from the prompt'));
  }

  function filterPrompt(s) {
    var out = '';
    for (var i = 0; i < s.length; i++) if (charToId[s.charAt(i)] != null) out += s.charAt(i);
    return out;
  }

  function restart() {
    if (gen.timer) { clearTimeout(gen.timer); gen.timer = null; }
    gen.st.n = 0;                    /* cache entries are overwritten as we re-feed */
    gen.ctxIds = [];
    gen.chars = [];
    gen.snaps = {};
    gen.snapOrder = [];
    gen.genCount = 0;
    gen.finished = false;
    gen.selected = -1;
    elText.textContent = '';
    var raw = elPrompt ? elPrompt.value : 'ROMEO: ';
    gen.promptUsed = raw;
    var p = filterPrompt(raw);
    if (!p) p = '\n';
    for (var i = 0; i < p.length; i++) emitChar(p.charAt(i), true);
  }

  function startRun() {
    var promptChanged = elPrompt && gen.promptUsed !== elPrompt.value;
    if (gen.chars.length === 0 || gen.finished || promptChanged) restart();
    gen.running = true;
    updateGoBtn();
    armTimer();
  }

  /* Force a character (bar click). Works running or paused; when paused it
     single-steps the generation, which is a nice way to walk the model. */
  function forceChar(tokId) {
    if (!(tokId >= 0 && tokId < V) || gen.chars.length === 0) return;  /* also rejects NaN */
    if (gen.timer) { clearTimeout(gen.timer); gen.timer = null; }
    emitChar(vocab.charAt(tokId), false);
    gen.genCount++;
    if (gen.running) {
      if (gen.genCount >= lengthTarget) { stopGen(true); return; }
      armTimer();
    }
  }

  /* ---- next-char probability bars ---- */
  var barEls = [];
  (function buildBars() {
    for (var k = 0; k < 10; k++) {
      var btn = document.createElement('button');
      btn.type = 'button';
      btn.className = 'tt-bar';
      var pct = document.createElement('span'); pct.className = 'tt-bar__pct';
      var track = document.createElement('span'); track.className = 'tt-bar__track';
      var fill = document.createElement('span'); fill.className = 'tt-bar__fill';
      var chv = document.createElement('span'); chv.className = 'tt-bar__ch';
      track.appendChild(fill);
      btn.appendChild(pct); btn.appendChild(track); btn.appendChild(chv);
      btn.addEventListener('click', function () { forceChar(+this.getAttribute('data-tok')); });
      elBars.appendChild(btn);
      barEls.push({ btn: btn, pct: pct, fill: fill, ch: chv });
    }
  })();

  function updateBars() {
    if (gen.chars.length === 0) return;
    softmax(gen.st.logits, temperature, gen.probs);
    sortBuf.sort(function (a, b) { return gen.probs[b] - gen.probs[a]; });
    for (var k = 0; k < 10 && k < V; k++) {
      var tok = sortBuf[k], p = gen.probs[tok], be = barEls[k];
      var label = dispChar(vocab.charAt(tok));
      be.btn.setAttribute('data-tok', tok);
      be.btn.title = 'Force "' + label + '" as the next character';
      be.fill.style.height = Math.max(2, p * 100) + '%';
      be.pct.textContent = (p * 100).toFixed(1) + '%';
      be.ch.textContent = label;
    }
  }

  /* ---- attention heatmaps (2 layers x 4 heads over the context window) ---- */
  function renderAttn(charIdx) {
    var meta = gen.chars[charIdx];
    var snap = gen.snaps[charIdx];
    if (!meta) return;
    if (!snap) {
      if (elCaption) elCaption.textContent =
        'attention snapshot for this character is gone (only the last ' + SNAP_MAX + ' are kept)';
      attnCtx.setTransform(1, 0, 0, 1, 0, 0);
      attnCtx.clearRect(0, 0, attnCanvas.width, attnCanvas.height);
      return;
    }
    if (elCaption) {
      elCaption.textContent = 'attention while ' + (meta.isPrompt ? 'reading' : 'writing') +
        ' "' + dispChar(meta.ch) + '" · one row per head, brighter = more attention · columns are the last ' +
        snap.ctx.length + ' characters';
    }
    drawSnap(snap);
  }

  function drawSnap(snap) {
    var n = snap.ctx.length, rows = snap.rows.length;
    var cssW = attnCanvas.clientWidth;
    if (!cssW || !n || !rows) return;
    var showChars = window.innerWidth >= 600;
    var gutter = 42, rowH = 15, gap = 3, charH = showChars ? 13 : 0;
    var cssH = rows * (rowH + gap) + charH + 2;
    var dpr = Math.min(window.devicePixelRatio || 1, 2);
    attnCanvas.style.height = cssH + 'px';
    var pw = Math.round(cssW * dpr), ph = Math.round(cssH * dpr);
    if (attnCanvas.width !== pw || attnCanvas.height !== ph) { attnCanvas.width = pw; attnCanvas.height = ph; }
    attnCtx.setTransform(dpr, 0, 0, dpr, 0, 0);
    attnCtx.clearRect(0, 0, cssW, cssH);

    var cellW = (cssW - gutter) / n;
    var lightT = isLight();
    var lo = lightT ? [235, 238, 242] : [28, 31, 38];
    var hi = lightT ? [52, 86, 138] : [130, 166, 204];
    var txtCol = lightT ? 'rgba(38,38,38,' : 'rgba(214,214,214,';
    var r, t, v;

    attnCtx.font = '9px ui-monospace, SFMono-Regular, Menlo, Consolas, monospace';
    for (r = 0; r < rows; r++) {
      var y = r * (rowH + gap);
      var row = snap.rows[r], mxv = 1e-9;
      for (t = 0; t < row.length; t++) if (row[t] > mxv) mxv = row[t];
      attnCtx.fillStyle = txtCol + '0.55)';
      attnCtx.textAlign = 'right';
      attnCtx.textBaseline = 'middle';
      attnCtx.fillText('L' + (Math.floor(r / net.nH) + 1) + '·h' + ((r % net.nH) + 1), gutter - 6, y + rowH / 2);
      for (t = 0; t < row.length; t++) {
        v = Math.pow(row[t] / mxv, 0.7);
        attnCtx.fillStyle = 'rgb(' +
          Math.round(lo[0] + (hi[0] - lo[0]) * v) + ',' +
          Math.round(lo[1] + (hi[1] - lo[1]) * v) + ',' +
          Math.round(lo[2] + (hi[2] - lo[2]) * v) + ')';
        attnCtx.fillRect(gutter + t * cellW, y, Math.max(1, cellW - 0.5), rowH);
      }
    }
    if (showChars) {
      var cy = rows * (rowH + gap) + charH / 2;
      var fs = Math.min(10, Math.max(6, cellW * 0.9));
      attnCtx.font = fs + 'px ui-monospace, SFMono-Regular, Menlo, Consolas, monospace';
      attnCtx.fillStyle = txtCol + '0.7)';
      attnCtx.textAlign = 'center';
      attnCtx.textBaseline = 'middle';
      for (t = 0; t < n; t++) {
        var c = snap.ctx.charAt(t);
        if (c === ' ') continue;
        attnCtx.fillText(c === '\n' ? '¶' : c, gutter + (t + 0.5) * cellW, cy + 1);
      }
    }
  }

  function redrawAttn() {
    if (gen.chars.length === 0) return;
    renderAttn(gen.selected >= 0 ? gen.selected : gen.chars.length - 1);
  }

  /* ---- click a written character to inspect its attention snapshot ---- */
  function selectChar(idx) {
    if (gen.selected >= 0 && gen.chars[gen.selected]) gen.chars[gen.selected].span.classList.remove('tt-ch--sel');
    gen.selected = idx;
    if (idx >= 0 && gen.chars[idx]) gen.chars[idx].span.classList.add('tt-ch--sel');
    renderAttn(idx >= 0 ? idx : gen.chars.length - 1);
  }
  elText.addEventListener('click', function (e) {
    var tEl = e.target;
    if (!tEl || !tEl.classList || !tEl.classList.contains('tt-ch')) return;
    var idx = +tEl.getAttribute('data-i');
    selectChar(gen.selected === idx ? -1 : idx);   /* click again to follow the newest */
  });

  /* ---- controls ---- */
  if (btnGo) btnGo.addEventListener('click', function () {
    if (gen.running) stopGen(false); else startRun();
  });
  if (elPrompt) elPrompt.addEventListener('keydown', function (e) {
    if (e.key === 'Enter') {
      e.preventDefault();
      stopGen(false);
      restart();
      gen.running = true;
      updateGoBtn();
      armTimer();
    }
  });
  if (elTemp) elTemp.addEventListener('input', function () {
    temperature = +elTemp.value;
    if (elTempV) elTempV.textContent = temperature.toFixed(2);
    updateBars();
  });
  if (elSpeed) elSpeed.addEventListener('input', function () {
    speed = Math.max(1, +elSpeed.value);
    if (elSpeedV) elSpeedV.textContent = String(Math.round(speed));
  });
  if (elLen) elLen.addEventListener('input', function () {
    lengthTarget = Math.round(+elLen.value);
    if (elLenV) elLenV.textContent = String(lengthTarget);
  });

  /* collapse / expand the controls (starts collapsed on small screens);
     the glyph + aria-label track the state, same as the Life page. */
  if (elCollapse && elPanel) {
    var setCollapsed = function (c) {
      elPanel.classList.toggle('tt-panel--collapsed', c);
      elCollapse.textContent = c ? '+' : '–';
      elCollapse.setAttribute('aria-label', c ? 'Show controls' : 'Hide controls');
    };
    elCollapse.addEventListener('click', function () {
      setCollapsed(!elPanel.classList.contains('tt-panel--collapsed'));
    });
    if (window.innerWidth < 600) setCollapsed(true);
  }

  /* ---- theme / resize / visibility ---- */
  try {
    new MutationObserver(redrawAttn)
      .observe(document.documentElement, { attributes: true, attributeFilter: ['data-theme'] });
  } catch (e) {}
  try {
    var mq = window.matchMedia('(prefers-color-scheme: light)');
    if (mq.addEventListener) mq.addEventListener('change', redrawAttn);
    else if (mq.addListener) mq.addListener(redrawAttn);
  } catch (e2) {}
  var rt;
  window.addEventListener('resize', function () { clearTimeout(rt); rt = setTimeout(redrawAttn, 150); });
  document.addEventListener('visibilitychange', function () {
    if (document.hidden) { if (gen.timer) { clearTimeout(gen.timer); gen.timer = null; } }
    else if (gen.running && !gen.timer) armTimer();
  });

  /* ---- init: prime the default prompt so the bars + attention are alive
     (a static computation; generation itself never auto-starts) ---- */
  if (elTempV) elTempV.textContent = temperature.toFixed(2);
  if (elSpeedV) elSpeedV.textContent = String(speed);
  if (elLenV) elLenV.textContent = String(lengthTarget);
  restart();
  updateGoBtn();
})();
